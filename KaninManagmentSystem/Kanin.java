// Klasse kanin.
// Kaniner har navn og farge.
class Kanin {

	// Kaninens navn.
	private String navn;

	// Kaninens farge.
	private String farge;

	// Returnerer kaninens navn.
	public String getNavn() {
		return navn;
	}

	// Setter variabelen "navn" til parameteret "n".
	public void setNavn(String n) {
		navn = n;
	}

	// Setter variabelen "farge" til parameteret "f".
	public void setFarge(String f) {
		farge = f;
	}
}